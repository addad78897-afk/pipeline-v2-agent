def is_second_instance(text: str) -> bool:
    """判断是否为二审案件审理经过。"""
    return bool(re.search(r'(?:向本院提起上诉|不服.{2,20}人民法院.{2,30}提起上诉|本院受理后依法组成合议庭)', text))


def apply_rules(text: str) -> str:
    """按顺序应用所有规范化规则。"""
    result = text.strip()

    for pattern, replacement in RULES:
        result = re.sub(pattern, replacement, result, flags=re.DOTALL)
        # 清理冗余空白
        result = re.sub(r' {2,}', ' ', result)
        result = re.sub(r'\s{2,}', ' ', result)

    # 后处理：修正可能引入的重复句号
    result = re.sub(r'[。.，,]{2,}', lambda m: m.group(0)[0], result)

    # 统一中文标点：确保使用中文标点
    result = result.replace('.,', '。')

    # 结尾规范化
    result = result.strip()
    if not result.endswith('。'):
        result += '。'

    return result


def optimize(text: str, judges: str = '', procedure: str = '') -> str:
    """优化单个审理经过文本。

    Args:
        text: 原始审理经过文本
        judges: 审理法官字段（用于二审合议庭补充）
        procedure: 审理程序字段（用于判断二审/再审/行政）
    """
    if not text or not isinstance(text, str):
        return ''

    text = text.strip()
    if not text:
        return ''

    # 步骤0：硬截断 — "本案现已审理终结" 之后的内容不属于审理经过
    for end_marker in ['本案现已审理终结', '本案现已审理终结', '本案审理终结']:
        idx = text.find(end_marker)
        if idx >= 0:
            cut_end = text.find('。', idx)
            if cut_end < 0:
                cut_end = idx + len(end_marker)
            else:
                cut_end += 1
            text = text[:cut_end]
            break

    if not text.strip():
        return ''

    # 步骤1-8：应用规范化规则
    text = apply_rules(text)

    # 步骤9：去除可能残留的冗余内容标记
    for noise_start in ['原告向本院提出诉讼', '原告诉称', '事实和理由', '事实与理由']:
        idx = text.find(noise_start)
        if idx > 100:
            text = text[:idx].strip()
            break

    # ------------------------------------------------------------------
    # 步骤10：合议庭成员补充（二审/再审案件）
    # ------------------------------------------------------------------
    if judges and judges.strip():
        # 仅二审/再审案件补充合议庭成员（一审独任制不补充）
        is_appeal = bool(re.search(r'(?:二审|再审|终审)', procedure))
        # 文本中已显式列出合议庭成员的，不再重复
        has_panel_in_text = '组成合议庭' in text

        if is_appeal and not has_panel_in_text:
            # 清洗法官列表
            judge_list = _parse_judge_list(judges)
            # 只有多名法官（合议庭）或明确组成合议庭才补充
            # 独任制（仅1名法官）不补充；仅合议庭（多名法官，以 ; 分隔）才补充
            if judge_list and ';' in judges:
                # 判断是否开庭审理
                if '不开庭' in text or '书面审理' in text or '经过阅卷' in text:
                    panel_suffix = f'本案由{judge_list}组成合议庭，不开庭进行了审理。'
                elif '庭询' in text or '询问' in text:
                    panel_suffix = f'本案由{judge_list}组成合议庭进行审理。'
                else:
                    panel_suffix = f'本案由{judge_list}组成合议庭进行审理。'

                # 插入到 "本案现已审理终结。" 之前
                if '本案现已审理终结' in text:
                    text = text.replace('本案现已审理终结。', panel_suffix)
                else:
                    text = text.rstrip('。') + '。' + panel_suffix

    # ------------------------------------------------------------------
    # 步骤11：移送管辖后处理
    # ------------------------------------------------------------------
    # 确保移送管辖文本中的 "本案于...立案受理" 前置
    text = re.sub(
        r'([一-鿿]{2,30}人民法院(?:作出|依法作出)[^，]+移送本院审理)[。.]?\s*本案于(\d{4}年\d{1,2}月\d{1,2}日)立案受理',
        r'\1。本案于\2立案受理',
        text
    )

    # 步骤12：确保以句号结尾
    text = text.strip().rstrip('，,。.')
    if text:
        text += '。'

    return text


def _parse_judge_list(judges_raw: str) -> str:
    """将审理法官字段解析为中文合议庭成员表述。

    '审判长 韦晓云; 审判员 张姝; 审判员 刘宏'
    → '审判长韦晓云、审判员张姝、审判员刘宏'
    """
    if not judges_raw:
        return ''
    parts = [p.strip() for p in judges_raw.split(';') if p.strip()]
    normalized = []
    for p in parts:
        # 去掉角色与名字之间的多余空格
        p = re.sub(r'(审判[员长]|代理审判员|人民陪审员)\s+', r'\1', p)
        normalized.append(p)
    return '、'.join(normalized)


# ===========================================================================
# 主流程
# ===========================================================================

def main():
    print(f"[INFO] 读取 Excel: {XLSX_PATH}")
    df = pd.read_excel(XLSX_PATH, dtype=str, keep_default_na=False)
    total = len(df)

    # 备份
    df.to_excel(XLSX_BAK, index=False, engine='openpyxl')
    print(f"[INFO] 备份 → {XLSX_BAK}")

    col = '审理经过'
    nonempty_mask = df[col] != ''
    nonempty_count = nonempty_mask.sum()
    print(f"[INFO] 审理经过 非空: {nonempty_count} / {total}")

    # 优化
    print(f"[INFO] 正在优化...")
    optimized = []
    samples = []

    for idx in range(total):
        old_val = df.at[idx, col]
        judges = str(df.at[idx, '审理法官'])
        procedure = str(df.at[idx, '审理程序'])
        new_val = optimize(str(old_val), judges=judges, procedure=procedure)

        df.at[idx, col] = new_val
        optimized.append(new_val)

        # 收集修改样本（保留有显著差异的）
        if old_val.strip() and old_val.strip() != new_val and len(samples) < 30:
            samples.append((idx, old_val.strip(), new_val))

    # 保存 Excel
    print(f"[INFO] 保存更新后的 Excel ...")
    df.to_excel(XLSX_PATH, index=False, engine='openpyxl')

    # 统计
    modified = sum(1 for o, n in zip(df[col][nonempty_mask], df[col][nonempty_mask])
                   if o.strip() != n.strip())
    # 用更直接的方法
    old_df = pd.read_excel(XLSX_BAK, dtype=str, keep_default_na=False)
    modified = (old_df[col] != df[col]).sum()

    avg_len_before = old_df[col][nonempty_mask].str.len().mean()
    avg_len_after = df[col][nonempty_mask].str.len().mean()

    # 输出报告
    print()
    print("=" * 70)
    print("  审理经过规范化优化报告")
    print("=" * 70)
    print(f"  总记录:        {total}")
    print(f"  非空记录:      {nonempty_count}")
    print(f"  已优化修改:    {modified}")
    print(f"  二审案件:      {old_df[col][nonempty_mask].apply(is_second_instance).sum()}")
    print(f"  平均长度:      {avg_len_before:.0f} → {avg_len_after:.0f} 字")
    print()

    # 打印对比示例
    print("=" * 70)
    print("  优化前后对比示例（10条不同类型的修改）")
    print("=" * 70)

    # 选取不同类型的修改展示
    shown = 0
    for idx, old, new in samples:
        if shown >= 10:
            break
        # 跳过变化很小的
        if abs(len(old) - len(new)) < 5 and old[:30] == new[:30]:
            continue
        shown += 1
        print(f"\n  [{shown}] 行{idx} (二审={is_second_instance(old)})")
        print(f"  修改前({len(old)}字): {old[:200]}")
        if len(old) > 200:
            print(f"  ...")
        print(f"  修改后({len(new)}字): {new[:200]}")
        if len(new) > 200:
            print(f"  ...")
        print(f"  {'─' * 60}")

    # 保存详细报告到 txt
    print(f"\n[INFO] 详细报告保存至: {OUTPUT_TXT}")
    with open(OUTPUT_TXT, 'w', encoding='utf-8') as f:
        f.write("=" * 70 + "\n")
        f.write("  审理经过规范化优化报告\n")
        f.write("=" * 70 + "\n\n")
        f.write(f"总记录: {total}\n")
        f.write(f"非空记录: {nonempty_count}\n")
        f.write(f"已优化修改: {modified}\n")
        f.write(f"平均长度: {avg_len_before:.0f} → {avg_len_after:.0f} 字\n\n")
        f.write("=" * 70 + "\n")
        f.write("  优化前后对比示例\n")
        f.write("=" * 70 + "\n\n")
        for i, (idx, old, new) in enumerate(samples[:30]):
            f.write(f"[{i+1}] 行{idx}\n")
            f.write(f"修改前({len(old)}字):\n{old}\n\n")
            f.write(f"修改后({len(new)}字):\n{new}\n\n")
            f.write("-" * 50 + "\n")

    print("\n完成。")


if __name__ == '__main__':
    main()
